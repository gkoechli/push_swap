#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char		*d;
	const unsigned char	*s;

	d = dst;
	s = src;
	if (dst == src)
		return (0);
	if (d == 0 && s == 0)
		return (dst);
	if (dst < src)
	{
		while (len-- > 0)
			*d++ = *s++;
	}
	else
	{
		while (len > 0)
		{
			d[len - 1] = s[len - 1];
			len--;
		}
	}
	return (dst);
}
